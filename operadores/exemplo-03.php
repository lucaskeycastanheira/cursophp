<?php
//Operador incremental
// Utilizar ++ e --, se usado depois da variável, irá apresentar na próxima apresentação e se antes já mostrará(pre incremento).

$v1=10;

echo $v1;
echo "<br>";

echo ++$v1;
echo "<br>";

echo $v1++;
echo "<br>";

echo $v1;
echo "<br>";

echo $v1--;
echo "<br>";

echo $v1;
echo "<br>";

echo --$v1;
echo "<br>";

//Precedência de operador, qual será executado primeiro?

//&& utilizando como "and"
//|| utilizando como "or" 
?>